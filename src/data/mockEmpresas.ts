export const getFilterOptions = (empresas: Empresa[]) => {
  const unique = <T>(arr: T[]): T[] => [...new Set(arr)];
  
  return {
    situacao: unique(empresas.map(e => e.situacao)),
    cnae_principal: unique(empresas.map(e => ({
      codigo: e.cnae_principal_codigo,
      nome: e.cnae_principal_nome
    }))).filter((v, i, a) => a.findIndex(t => t.codigo === v.codigo) === i),
    porte: unique(empresas.map(e => e.porte)),
    tipo: unique(empresas.map(e => e.tipo)),
    matriz_filial: unique(empresas.map(e => e.matriz_filial)),
    estado: unique(empresas.map(e => e.estado)),
    municipio: unique(empresas.map(e => e.municipio)),
    mei: [true, false],
    simples: [true, false]
  };
};
